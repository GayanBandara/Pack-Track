package model.models;

/**
 * Entity class for train object
 * 
 * @author Dilanka1
 * 
 */
public class Train {

	String ID;
	String name;

	public String getID() {
		return ID;
	}

	public void setID(String iD) {
		ID = iD;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
